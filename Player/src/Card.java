public class Card {
    private int fruitKind;
    private int fruitNum;

    public int getFruitKind() {
        return fruitKind;
    }

    public int getFruitNum() {
        return fruitNum;
    }

    public Card(int fruitKind, int fruitNum) {
        super();
        this.fruitKind = fruitKind;
        this.fruitNum = fruitNum;
    }

}
